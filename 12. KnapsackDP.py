"""
Task: Implementation and Time Analysis of 0/1 Knapsack Problem using Dynamic Programming
"""
def knapsack(W, wt, val, n):
    dp = [[0]*(W+1) for _ in range(n+1)]
    for i in range(n+1):
        for w in range(W+1):
            if i == 0 or w == 0:
                dp[i][w] = 0
            elif wt[i-1] <= w:
                dp[i][w] = max(val[i-1] + dp[i-1][w-wt[i-1]], dp[i-1][w])
            else:
                dp[i][w] = dp[i-1][w]
    return dp[n][W]
if __name__ == "__main__":
    import time
    val = [60, 100, 120]
    wt = [10, 20, 30]
    W = 50
    n = len(val)
    start = time.time()
    res = knapsack(W, wt, val, n)
    end = time.time()
    print(f"Max value: {res}, Time: {end-start:.6f}s")
"""
Time Complexity: O(nW)
Space Complexity: O(nW)
"""
